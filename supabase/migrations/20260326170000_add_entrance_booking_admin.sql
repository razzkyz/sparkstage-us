GRANT EXECUTE ON FUNCTION public.ensure_all_ticket_availability_coverage() TO service_role;
CREATE OR REPLACE FUNCTION public.reserve_ticket_capacity(
  p_ticket_id BIGINT,
  p_date DATE,
  p_time_slot TEXT,
  p_quantity INTEGER
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF p_quantity IS NULL OR p_quantity <= 0 THEN
    RETURN false;
  END IF;

  WITH target AS (
    SELECT
      ta.id,
      ta.reserved_capacity,
      ta.sold_capacity,
      COALESCE(so.capacity_override, dlo.capacity_override, ta.total_capacity) AS effective_total_capacity,
      COALESCE(so.is_closed, dlo.is_closed, false) AS is_closed
    FROM public.ticket_availabilities ta
    LEFT JOIN public.ticket_availability_overrides so
      ON so.ticket_id = ta.ticket_id
     AND so.date = ta.date
     AND so.time_slot = ta.time_slot
    LEFT JOIN public.ticket_availability_overrides dlo
      ON dlo.ticket_id = ta.ticket_id
     AND dlo.date = ta.date
     AND dlo.time_slot IS NULL
    WHERE ta.ticket_id = p_ticket_id
      AND ta.date = p_date
      AND ta.time_slot IS NOT DISTINCT FROM (p_time_slot::TIME)
    FOR UPDATE
  )
  UPDATE public.ticket_availabilities ta
  SET
    reserved_capacity = ta.reserved_capacity + p_quantity,
    updated_at = NOW()
  FROM target
  WHERE ta.id = target.id
    AND target.is_closed = false
    AND (target.effective_total_capacity - target.reserved_capacity - target.sold_capacity) >= p_quantity;

  RETURN FOUND;
END;
$$;
