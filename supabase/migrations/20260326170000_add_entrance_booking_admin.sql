CREATE TABLE IF NOT EXISTS public.ticket_booking_settings (
  ticket_id BIGINT PRIMARY KEY REFERENCES public.tickets(id) ON DELETE CASCADE,
  max_tickets_per_booking INTEGER NOT NULL DEFAULT 5 CHECK (max_tickets_per_booking > 0),
  booking_window_days INTEGER NOT NULL DEFAULT 30 CHECK (booking_window_days > 0),
  auto_generate_days_ahead INTEGER NOT NULL DEFAULT 60 CHECK (auto_generate_days_ahead >= 0),
  default_slot_capacity INTEGER NOT NULL DEFAULT 100 CHECK (default_slot_capacity > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
INSERT INTO public.ticket_booking_settings (
  ticket_id,
  max_tickets_per_booking,
  booking_window_days,
  auto_generate_days_ahead,
  default_slot_capacity
)
SELECT
  t.id,
  5,
  30,
  CASE WHEN t.type = 'entrance' THEN 60 ELSE 0 END,
  100
FROM public.tickets t
WHERE NOT EXISTS (
  SELECT 1
  FROM public.ticket_booking_settings s
  WHERE s.ticket_id = t.id
);
