CREATE OR REPLACE FUNCTION public.list_ticket_time_slots(p_ticket_id BIGINT)
RETURNS TABLE (time_slot TIME)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT DISTINCT TRIM(slot.value)::TIME AS time_slot
  FROM public.tickets t
  CROSS JOIN LATERAL json_array_elements_text(COALESCE(t.time_slots, '[]'::json)) AS slot(value)
  WHERE t.id = p_ticket_id
    AND TRIM(slot.value) <> ''
  ORDER BY 1;
$$;
CREATE OR REPLACE FUNCTION public.list_effective_ticket_availabilities(
  p_ticket_id BIGINT,
  p_start_date DATE DEFAULT NULL,
  p_end_date DATE DEFAULT NULL
)
RETURNS TABLE (
  id BIGINT,
  ticket_id BIGINT,
  date DATE,
  time_slot TIME,
  base_total_capacity INTEGER,
  effective_total_capacity INTEGER,
  reserved_capacity INTEGER,
  sold_capacity INTEGER,
  available_capacity INTEGER,
  is_closed BOOLEAN,
  reason TEXT
)
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  WITH slot_overrides AS (
    SELECT
      o.ticket_id,
      o.date,
      o.time_slot,
      o.is_closed,
      o.capacity_override,
      o.reason
    FROM public.ticket_availability_overrides o
    WHERE o.ticket_id = p_ticket_id
      AND o.time_slot IS NOT NULL
  ),
  date_level_overrides AS (
    SELECT
      o.ticket_id,
      o.date,
      o.is_closed,
      o.capacity_override,
      o.reason
    FROM public.ticket_availability_overrides o
    WHERE o.ticket_id = p_ticket_id
      AND o.time_slot IS NULL
  )
  SELECT
    ta.id,
    ta.ticket_id,
    ta.date,
    ta.time_slot,
    ta.total_capacity AS base_total_capacity,
    COALESCE(so.capacity_override, dlo.capacity_override, ta.total_capacity) AS effective_total_capacity,
    ta.reserved_capacity,
    ta.sold_capacity,
    CASE
      WHEN COALESCE(so.is_closed, dlo.is_closed, false) THEN 0
      ELSE GREATEST(
        COALESCE(so.capacity_override, dlo.capacity_override, ta.total_capacity) - ta.sold_capacity,
        0
      )
    END AS available_capacity,
    COALESCE(so.is_closed, dlo.is_closed, false) AS is_closed,
    COALESCE(so.reason, dlo.reason) AS reason
  FROM public.ticket_availabilities ta
  LEFT JOIN slot_overrides so
    ON so.ticket_id = ta.ticket_id
   AND so.date = ta.date
   AND so.time_slot = ta.time_slot
  LEFT JOIN date_level_overrides dlo
    ON dlo.ticket_id = ta.ticket_id
   AND dlo.date = ta.date
  WHERE ta.ticket_id = p_ticket_id
    AND (p_start_date IS NULL OR ta.date >= p_start_date)
    AND (p_end_date IS NULL OR ta.date <= p_end_date)
  ORDER BY ta.date ASC, ta.time_slot ASC;
$$;
GRANT EXECUTE ON FUNCTION public.list_ticket_time_slots(BIGINT) TO anon, authenticated, service_role;
GRANT EXECUTE ON FUNCTION public.list_effective_ticket_availabilities(BIGINT, DATE, DATE) TO anon, authenticated, service_role;
CREATE OR REPLACE FUNCTION public.generate_ticket_availability(
  p_start_date DATE,
  p_end_date DATE,
  p_ticket_id BIGINT DEFAULT 1,
  p_total_capacity INTEGER DEFAULT NULL
)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  inserted_count INTEGER := 0;
  effective_start_date DATE;
  effective_end_date DATE;
  configured_capacity INTEGER;
  slot_count INTEGER;
BEGIN
  IF p_start_date IS NULL OR p_end_date IS NULL THEN
    RAISE EXCEPTION 'Start date and end date are required';
  END IF;

  IF p_start_date > p_end_date THEN
    RAISE EXCEPTION 'Start date must be before or equal to end date';
  END IF;

  IF auth.uid() IS NOT NULL AND NOT public.is_admin() THEN
    RAISE EXCEPTION 'Only admins can generate ticket availability';
  END IF;

  SELECT
    GREATEST(p_start_date, t.available_from::DATE),
    LEAST(p_end_date, t.available_until::DATE),
    COALESCE(p_total_capacity, s.default_slot_capacity)
  INTO
    effective_start_date,
    effective_end_date,
    configured_capacity
  FROM public.tickets t
  JOIN public.ticket_booking_settings s ON s.ticket_id = t.id
  WHERE t.id = p_ticket_id;

  IF effective_start_date IS NULL OR effective_end_date IS NULL OR effective_start_date > effective_end_date THEN
    RETURN 0;
  END IF;

  IF configured_capacity IS NULL OR configured_capacity <= 0 THEN
    RAISE EXCEPTION 'Total capacity must be greater than zero';
  END IF;

  SELECT COUNT(*)
  INTO slot_count
  FROM public.list_ticket_time_slots(p_ticket_id);

  IF slot_count = 0 THEN
    RAISE EXCEPTION 'Ticket % has no time slots configured', p_ticket_id;
  END IF;

  WITH days AS (
    SELECT generate_series(effective_start_date, effective_end_date, INTERVAL '1 day')::DATE AS service_date
  ),
  slots AS (
    SELECT time_slot FROM public.list_ticket_time_slots(p_ticket_id)
  ),
  inserted AS (
    INSERT INTO public.ticket_availabilities (
      ticket_id,
      date,
      time_slot,
      total_capacity,
      reserved_capacity,
      sold_capacity,
      version,
      created_at,
      updated_at
    )
    SELECT
      p_ticket_id,
      d.service_date,
      s.time_slot,
      configured_capacity,
      0,
      0,
      0,
      NOW(),
      NOW()
    FROM days d
    CROSS JOIN slots s
    ON CONFLICT (ticket_id, date, time_slot) DO NOTHING
    RETURNING 1
  )
  SELECT COUNT(*)
  INTO inserted_count
  FROM inserted;

  RETURN inserted_count;
END;
$$;
CREATE OR REPLACE FUNCTION public.regenerate_ticket_availability(
  p_start_date DATE,
  p_end_date DATE,
  p_ticket_id BIGINT DEFAULT 1,
  p_total_capacity INTEGER DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  effective_start_date DATE;
  effective_end_date DATE;
  configured_capacity INTEGER;
  slot_times TIME[];
  inserted_count INTEGER := 0;
  updated_count INTEGER := 0;
  deleted_count INTEGER := 0;
  skipped_locked_count INTEGER := 0;
BEGIN
  IF p_start_date IS NULL OR p_end_date IS NULL THEN
    RAISE EXCEPTION 'Start date and end date are required';
  END IF;

  IF p_start_date > p_end_date THEN
    RAISE EXCEPTION 'Start date must be before or equal to end date';
  END IF;

  IF auth.uid() IS NOT NULL AND NOT public.is_admin() THEN
    RAISE EXCEPTION 'Only admins can regenerate ticket availability';
  END IF;

  SELECT
    GREATEST(p_start_date, t.available_from::DATE),
    LEAST(p_end_date, t.available_until::DATE),
    COALESCE(p_total_capacity, s.default_slot_capacity)
  INTO
    effective_start_date,
    effective_end_date,
    configured_capacity
  FROM public.tickets t
  JOIN public.ticket_booking_settings s ON s.ticket_id = t.id
  WHERE t.id = p_ticket_id;

  IF effective_start_date IS NULL OR effective_end_date IS NULL OR effective_start_date > effective_end_date THEN
    RETURN jsonb_build_object(
      'inserted', 0,
      'updated', 0,
      'deleted', 0,
      'skipped_locked', 0
    );
  END IF;

  SELECT ARRAY_AGG(time_slot ORDER BY time_slot)
  INTO slot_times
  FROM public.list_ticket_time_slots(p_ticket_id);

  IF slot_times IS NULL OR array_length(slot_times, 1) IS NULL THEN
    RAISE EXCEPTION 'Ticket % has no time slots configured', p_ticket_id;
  END IF;

  WITH days AS (
    SELECT generate_series(effective_start_date, effective_end_date, INTERVAL '1 day')::DATE AS service_date
  ),
  desired AS (
    SELECT d.service_date, slot_time AS time_slot
    FROM days d
    CROSS JOIN unnest(slot_times) AS slot_time
  ),
  inserted AS (
    INSERT INTO public.ticket_availabilities (
      ticket_id,
      date,
      time_slot,
      total_capacity,
      reserved_capacity,
      sold_capacity,
      version,
      created_at,
      updated_at
    )
    SELECT
      p_ticket_id,
      desired.service_date,
      desired.time_slot,
      configured_capacity,
      0,
      0,
      0,
      NOW(),
      NOW()
    FROM desired
    LEFT JOIN public.ticket_availabilities ta
      ON ta.ticket_id = p_ticket_id
     AND ta.date = desired.service_date
     AND ta.time_slot = desired.time_slot
    WHERE ta.id IS NULL
    RETURNING 1
  ),
  updated AS (
    UPDATE public.ticket_availabilities ta
    SET
      total_capacity = configured_capacity,
      updated_at = NOW()
    FROM desired
    WHERE ta.ticket_id = p_ticket_id
      AND ta.date = desired.service_date
      AND ta.time_slot = desired.time_slot
      AND ta.reserved_capacity = 0
      AND ta.sold_capacity = 0
      AND ta.total_capacity <> configured_capacity
    RETURNING ta.id
  ),
  deleted AS (
    DELETE FROM public.ticket_availabilities ta
    WHERE ta.ticket_id = p_ticket_id
      AND ta.date BETWEEN effective_start_date AND effective_end_date
      AND ta.time_slot IS NOT NULL
      AND ta.reserved_capacity = 0
      AND ta.sold_capacity = 0
      AND NOT EXISTS (
        SELECT 1
        FROM unnest(slot_times) AS desired_slot
        WHERE desired_slot = ta.time_slot
      )
    RETURNING ta.id
  ),
  locked AS (
    SELECT ta.id
    FROM public.ticket_availabilities ta
    WHERE ta.ticket_id = p_ticket_id
      AND ta.date BETWEEN effective_start_date AND effective_end_date
      AND (
        (ta.time_slot = ANY(slot_times) AND (ta.reserved_capacity > 0 OR ta.sold_capacity > 0))
        OR (
          ta.time_slot IS NOT NULL
          AND NOT (ta.time_slot = ANY(slot_times))
          AND (ta.reserved_capacity > 0 OR ta.sold_capacity > 0)
        )
      )
  )
  SELECT
    (SELECT COUNT(*) FROM inserted),
    (SELECT COUNT(*) FROM updated),
    (SELECT COUNT(*) FROM deleted),
    (SELECT COUNT(*) FROM locked)
  INTO
    inserted_count,
    updated_count,
    deleted_count,
    skipped_locked_count;

  RETURN jsonb_build_object(
    'inserted', inserted_count,
    'updated', updated_count,
    'deleted', deleted_count,
    'skipped_locked', skipped_locked_count
  );
END;
$$;
CREATE OR REPLACE FUNCTION public.ensure_ticket_availability_coverage(
  p_ticket_id BIGINT DEFAULT 1,
  p_days_ahead INTEGER DEFAULT NULL,
  p_total_capacity INTEGER DEFAULT NULL
)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  configured_days_ahead INTEGER;
BEGIN
  SELECT COALESCE(p_days_ahead, s.auto_generate_days_ahead)
  INTO configured_days_ahead
  FROM public.ticket_booking_settings s
  WHERE s.ticket_id = p_ticket_id;

  IF configured_days_ahead IS NULL OR configured_days_ahead <= 0 THEN
    RETURN 0;
  END IF;

  RETURN public.generate_ticket_availability(
    CURRENT_DATE,
    CURRENT_DATE + configured_days_ahead,
    p_ticket_id,
    p_total_capacity
  );
END;
$$;
CREATE OR REPLACE FUNCTION public.ensure_all_ticket_availability_coverage()
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  total_inserted INTEGER := 0;
  ticket_row RECORD;
BEGIN
  FOR ticket_row IN
    SELECT s.ticket_id, s.auto_generate_days_ahead, s.default_slot_capacity
    FROM public.ticket_booking_settings s
    WHERE s.auto_generate_days_ahead > 0
  LOOP
    total_inserted := total_inserted + public.ensure_ticket_availability_coverage(
      ticket_row.ticket_id,
      ticket_row.auto_generate_days_ahead,
      ticket_row.default_slot_capacity
    );
  END LOOP;

  RETURN total_inserted;
END;
$$;
