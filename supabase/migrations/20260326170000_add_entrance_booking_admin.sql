CREATE OR REPLACE FUNCTION public.enforce_ticket_availability_override_safety()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  conflicting_rows INTEGER := 0;
BEGIN
  SELECT COUNT(*)
  INTO conflicting_rows
  FROM public.ticket_availabilities ta
  WHERE ta.ticket_id = NEW.ticket_id
    AND ta.date = NEW.date
    AND (NEW.time_slot IS NULL OR ta.time_slot IS NOT DISTINCT FROM NEW.time_slot)
    AND (
      (NEW.is_closed = true AND (ta.reserved_capacity > 0 OR ta.sold_capacity > 0))
      OR (
        NEW.capacity_override IS NOT NULL
        AND NEW.capacity_override < (ta.reserved_capacity + ta.sold_capacity)
      )
    );

  IF conflicting_rows > 0 THEN
    RAISE EXCEPTION 'Override conflicts with sold or reserved capacity';
  END IF;

  RETURN NEW;
END;
$$;
